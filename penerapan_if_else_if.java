public class penerapan_if_else_if {
    public static void main(String[] args) {
        int number = 10;

        if (number < 0) {
            System.out.println("angkanya positif");
        } else if (number < 0) {
            System.out.println("angkanya megatif");
        } else {
            System.out.println("angkanya nol");
        }
    }
}
